// Helper: find product by id
function getProduct(id) { return LDM_PRODUCTS.find(p => p.id === id); }

// Cart helpers (localStorage)
const Cart = {
  key: 'ldm_cart',
  get() { try { return JSON.parse(localStorage.getItem(this.key)) || []; } catch(e) { return []; } },
  save(items) { localStorage.setItem(this.key, JSON.stringify(items)); },
  add(productId, qty = 1) {
    const items = this.get();
    const idx = items.findIndex(i => i.id === productId);
    if (idx > -1) { items[idx].qty += qty; } else { items.push({ id: productId, qty }); }
    this.save(items);
    this.updateBadge();
  },
  remove(productId) {
    const items = this.get().filter(i => i.id !== productId);
    this.save(items);
    this.updateBadge();
  },
  setQty(productId, qty) {
    const items = this.get();
    const idx = items.findIndex(i => i.id === productId);
    if (idx > -1) { if (qty < 1) { items.splice(idx, 1); } else { items[idx].qty = qty; } }
    this.save(items);
    this.updateBadge();
  },
  count() { return this.get().reduce((s, i) => s + i.qty, 0); },
  total() {
    return this.get().reduce((s, i) => {
      const p = getProduct(i.id);
      return s + (p ? p.price * i.qty : 0);
    }, 0);
  },
  clear() { localStorage.removeItem(this.key); this.updateBadge(); },
  updateBadge() {
    const cnt = this.count();
    document.querySelectorAll('.cart-badge').forEach(el => {
      el.textContent = cnt;
      el.style.display = cnt > 0 ? 'flex' : 'none';
    });
  }
};